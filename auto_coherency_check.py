# STREAMING VALIDATION
# ══════════════════════════════════════════════════════════════════════════════

class StreamValidator:
    """Validates datasets as they're generated (infinite procedural support)."""

    def __init__(self, error_threshold: float = 0.05,
                 check_interval: int = 100,
                 auto_fix: bool = True):
        """
        Args:
            error_threshold: Stop if error rate exceeds this (0.05 = 5%)
            check_interval: Validate every N samples
            auto_fix: Attempt to fix common errors automatically
        """
        self.error_threshold = error_threshold
        self.check_interval = check_interval
        self.auto_fix = auto_fix

        self.total_generated = 0
        self.total_valid = 0
        self.total_errors = 0
        self.checker = CoherencyChecker()

    def validate_stream(self, generator: Generator[str, None, None],
                       max_samples: int = None) -> Generator[str, None, None]:
        """
        Validate samples from a generator as they're produced.
        Yields only valid samples.

        Args:
            generator: Generator yielding dataset entries
            max_samples: Stop after this many samples (None = infinite)

        Yields:
            Valid dataset entries
        """
        batch = []

        for sample in generator:
            self.total_generated += 1
            batch.append(sample)

            # Check batch periodically
            if len(batch) >= self.check_interval:
                valid_samples = self._validate_batch(batch)
                for valid in valid_samples:
                    yield valid
                batch = []

            # Stop if we hit error threshold
            if self.total_generated > 0:
                error_rate = self.total_errors / self.total_generated
                if error_rate > self.error_threshold:
                    print(f"\n[StreamValidator] Error threshold exceeded: "
                          f"{error_rate:.1%} > {self.error_threshold:.1%}")
                    print(f"[StreamValidator] Generated {self.total_generated} samples, "
                          f"{self.total_valid} valid, {self.total_errors} errors")
                    break

            # Stop if we hit max samples
            if max_samples and self.total_generated >= max_samples:
                break

        # Process remaining batch
        if batch:
            valid_samples = self._validate_batch(batch)
            for valid in valid_samples:
                yield valid

    def _validate_batch(self, batch: List[str]) -> List[str]:
        """Validate a batch of samples. Returns valid samples."""
        valid = []

        for sample in batch:
            # Quick validation
            result = self.checker.check_text(sample)

            errors = sum(1 for i in result.issues if i.severity == "error")

            if errors == 0:
                self.total_valid += 1
                valid.append(sample)
            else:
                self.total_errors += 1

                # Try auto-fix if enabled
                if self.auto_fix:
                    fixed = self._try_fix(sample, result)
                    if fixed:
                        # Re-validate
                        recheck = self.checker.check_text(fixed)
                        recheck_errors = sum(1 for i in recheck.issues
                                           if i.severity == "error")
                        if recheck_errors == 0:
                            self.total_valid += 1
                            self.total_errors -= 1  # Fixed the error
                            valid.append(fixed)

        return valid

    def _try_fix(self, sample: str, result: CheckResult) -> str:
        """Attempt to auto-fix common errors."""
        fixed = sample

        # Fix missing start token
        if not sample.startswith("<|startoftext|>"):
            fixed = "<|startoftext|>" + fixed

        # Fix missing end token
        if not sample.endswith("<|endoftext|>"):
            fixed = fixed + "<|endoftext|>"

        # Note: More complex fixes (JSON structure, math errors) are harder
        # to auto-fix without context, so we skip those

        return fixed if fixed != sample else None

    def stats(self) -> dict:
        """Return validation statistics."""
        error_rate = self.total_errors / max(1, self.total_generated)
        success_rate = self.total_valid / max(1, self.total_generated)

        return {
            "total_generated": self.total_generated,
            "total_valid": self.total_valid,
            "total_errors": self.total_errors,
            "error_rate": error_rate,
            "success_rate": success_rate,
        }


