"""
auto_coherency_check.py
========================
Automatically validates datasets generated by train_avus_kaggle.py generators.

Features:
  - Generates synthetic datasets on-the-fly
  - Validates them in real-time during generation
  - Stops generation if error rate exceeds threshold
  - Produces cleaned, validated datasets ready for training
  - Works with procedural infinite datasets (streaming validation)

Usage:
  # Validate existing generators
  python auto_coherency_check.py --validate-generators

  # Generate and validate a clean dataset
  python auto_coherency_check.py --generate --samples 1000 --output clean_dataset.txt

  # Stream-validate procedural dataset (checks as it generates)
  python auto_coherency_check.py --procedural --samples 10000 --difficulty 3
"""

import sys
import json
import random
import argparse
from pathlib import Path
from typing import List, Generator, Tuple
from coherency_checker import CoherencyChecker, CheckResult


