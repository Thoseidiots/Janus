def validate_generators():
    """Validate the built-in generators."""
    print("Validating built-in dataset generators...\n")

    generators = [
        ("3D Generation", generate_3d_pairs, 100),
        ("Screen Actions", generate_screen_action_pairs, 100),
        ("Language", generate_language_pairs, 100),
        ("Reasoning", generate_reasoning_pairs, 100),
    ]

    for name, gen_func, samples in generators:
        print(f"[{name}]")
        data = gen_func(samples)

        checker = CoherencyChecker()
        text = "\n".join(data)
        result = checker.check_text(text)

        errors = sum(1 for i in result.issues if i.severity == "error")
        warnings = sum(1 for i in result.issues if i.severity == "warning")

        print(f"  Samples: {samples}")
        print(f"  Valid: {result.valid_entries}/{result.total_entries}")
        print(f"  Success Rate: {(result.valid_entries/max(1,result.total_entries)*100):.1f}%")
        print(f"  Errors: {errors}, Warnings: {warnings}")
        print()


def generate_clean_dataset(samples: int, output: str):
    """Generate a validated clean dataset."""
    print(f"Generating {samples} clean samples...\n")

    # Generate mixed dataset
    generators = [
        ("3D", generate_3d_pairs),
        ("Screen", generate_screen_action_pairs),
        ("Language", generate_language_pairs),
        ("Reasoning", generate_reasoning_pairs),
    ]

    all_samples = []
    per_type = samples // len(generators)

    for name, gen_func in generators:
        print(f"  Generating {per_type} {name} samples...")
        batch = gen_func(per_type)
        all_samples.extend(batch)

    random.shuffle(all_samples)

    # Validate
    print("\nValidating...")
    checker = CoherencyChecker()
    text = "\n".join(all_samples)
    result = checker.check_text(text)

    # Filter out invalid entries
    valid_samples = []
    for i, sample in enumerate(all_samples, start=1):
        # Check if this line had errors
        line_errors = [issue for issue in result.issues
                      if issue.line_num == i and issue.severity == "error"]
        if not line_errors:
            valid_samples.append(sample)

    # Write output
    output_path = Path(output)
    output_path.write_text("\n".join(valid_samples))

    print(f"\n{'='*70}")
    print(f"Generated: {len(all_samples)} samples")
    print(f"Valid: {len(valid_samples)} samples ({len(valid_samples)/len(all_samples)*100:.1f}%)")
    print(f"Saved to: {output_path}")
    print(f"{'='*70}")


def stream_procedural_dataset(samples: int, difficulty: int, output: str = None):
    """Generate and stream-validate procedural dataset."""
    print(f"Generating procedural dataset (difficulty={difficulty})...\n")

    validator = StreamValidator(
        error_threshold=0.05,  # Stop if >5% error rate
        check_interval=100,    # Validate every 100 samples
        auto_fix=True          # Try to fix common errors
    )

    gen = procedural_generator(difficulty=difficulty, seed=42)
    valid_samples = []

    print("Streaming validation...")
    for i, sample in enumerate(validator.validate_stream(gen, max_samples=samples)):
        valid_samples.append(sample)

        if (i + 1) % 500 == 0:
            stats = validator.stats()
            print(f"  Generated: {stats['total_generated']} | "
                  f"Valid: {stats['total_valid']} | "
                  f"Errors: {stats['total_errors']} | "
                  f"Success: {stats['success_rate']:.1%}")

    # Final stats
    stats = validator.stats()
    print(f"\n{'='*70}")
    print(f"Procedural Generation Complete")
    print(f"{'='*70}")
    print(f"Generated: {stats['total_generated']}")
    print(f"Valid: {stats['total_valid']}")
    print(f"Errors: {stats['total_errors']}")
    print(f"Success Rate: {stats['success_rate']:.1%}")

    if output:
        output_path = Path(output)
        output_path.write_text("\n".join(valid_samples))
        print(f"Saved to: {output_path}")

    print(f"{'='*70}")


def main():
    parser = argparse.ArgumentParser(
        description="Auto-validate synthetic datasets for Janus training"
    )

    parser.add_argument("--validate-generators", action="store_true",
                       help="Validate all built-in generators")
    parser.add_argument("--generate", action="store_true",
                       help="Generate a clean validated dataset")
    parser.add_argument("--procedural", action="store_true",
                       help="Generate procedural infinite dataset with streaming validation")
    parser.add_argument("--samples", type=int, default=1000,
                       help="Number of samples to generate")
    parser.add_argument("--difficulty", type=int, default=1,
                       help="Difficulty level for procedural generation (1-5)")
    parser.add_argument("--output", type=str, default="clean_dataset.txt",
                       help="Output file path")

    args = parser.parse_args()

    if args.validate_generators:
        validate_generators()
    elif args.generate:
        generate_clean_dataset(args.samples, args.output)
    elif args.procedural:
        stream_procedural_dataset(args.samples, args.difficulty, args.output)
    else:
        parser.print_help()


if __name__ == "__main__":
    main()
