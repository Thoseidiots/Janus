def procedural_generator(difficulty: int = 1, seed: int = None) -> Generator[str, None, None]:
    """
    Infinite procedural dataset generator (like No Man's Sky).

    Args:
        difficulty: 1-5 (affects complexity of generated data)
        seed: Random seed for reproducibility

    Yields:
        Infinite stream of dataset entries
    """
    if seed is not None:
        random.seed(seed)

    generators = [
        generate_3d_pairs,
        generate_screen_action_pairs,
        generate_language_pairs,
        generate_reasoning_pairs,
    ]

    while True:
        # Pick a random generator type
        gen_func = _rc(generators)

        # Generate a batch
        batch_size = 100 * difficulty  # More difficult = larger batches
        batch = gen_func(batch_size)

        for sample in batch:
            yield sample


