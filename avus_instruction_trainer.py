if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Train Avus on instruction data")
    parser.add_argument("--epochs",      type=int,   default=3)
    parser.add_argument("--samples",     type=int,   default=50_000)
    parser.add_argument("--batch-size",  type=int,   default=4)
    parser.add_argument("--lr",          type=float, default=3e-5)
    parser.add_argument("--block-size",  type=int,   default=256)
    parser.add_argument("--grad-accum",  type=int,   default=4)
    parser.add_argument("--weights-in",  type=str,   default=None,
                        help="Path to existing weights to fine-tune from")
    parser.add_argument("--weights-out", type=str,   default="avus_instruct.pt")
    parser.add_argument("--resume",      action="store_true",
                        help="Resume from avus_instruct.pt if it exists")
    parser.add_argument("--test",        action="store_true",
                        help="Run generation test after training")
    args = parser.parse_args()

    if args.resume and Path("avus_instruct.pt").exists():
        args.weights_in = "avus_instruct.pt"

    model = train(
        epochs     = args.epochs,
        samples    = args.samples,
        batch_size = args.batch_size,
        lr         = args.lr,
        block_size = args.block_size,
        weights_in = args.weights_in,
        weights_out= args.weights_out,
        grad_accum = args.grad_accum,
    )

    if args.test:
        test_generation(args.weights_out)
