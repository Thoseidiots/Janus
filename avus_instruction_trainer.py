"""
avus_instruction_trainer.py
============================
Fine-tunes Avus on instruction-following data generated by
avus_instruction_dataset.py.

No API keys. No cloud services. Runs entirely locally on CPU or GPU.
Uses the existing Avus architecture from avus.py.

Usage:
    python avus_instruction_trainer.py
    python avus_instruction_trainer.py --epochs 5 --samples 100000
    python avus_instruction_trainer.py --resume --weights avus_instruct.pt
"""

